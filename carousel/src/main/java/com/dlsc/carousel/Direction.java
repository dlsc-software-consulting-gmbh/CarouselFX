package com.dlsc.carousel;

/**
 * Represents the direction of a page transition in the carousel.
 */
public enum Direction {

    /**
     * Transition moves forward (e.g., left-to-right or next page).
     */
    FORWARD,

    /**
     * Transition moves backward (e.g., right-to-left or previous page).
     */
    BACKWARD
}
