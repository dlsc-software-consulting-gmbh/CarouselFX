package com.dlsc.carousel;

/**
 * Controls the visibility behavior of UI elements such as navigation arrows
 * and the bottom navigator.
 */
public enum DisplayMode {

    /**
     * Always visible.
     */
    SHOW,

    /**
     * Always hidden.
     */
    HIDE,

    /**
     * Visible on mouse hover, hidden otherwise.
     */
    AUTO
}
